    public void iniciar() {
        List<Cliente> clientes = controller.getAllClientes();
       
        DefaultTableModel tableModel = (DefaultTableModel) getjTable1().getModel();
        tableModel.setNumRows(0);
        
        for(Cliente cliente : clientes) {
            tableModel.addRow(new Object[]{
                cliente.getNome(),
                cliente.getNumero(),
                cliente.getEndereco(),
                cliente.getCep(),
            });
        }
        
        }
    

